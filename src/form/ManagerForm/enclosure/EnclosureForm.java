    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {                                         
       	if(jTable1.getSelectedRows().length == 0) {
    		JOptionPane.showMessageDialog(this, "Vui lòng chọn đối tượng để xóa");
    		return;
    	}
        int result = JOptionPane.showOptionDialog(this,
                "Bạn có chắc muốn xóa ?",
                "Xác nhận",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                null,
                null);
        int[] stt = jTable1.getSelectedRows();
        if(result == 0) {
        	try {
        		int id = listEl.get(jTable1.getSelectedRow()).getId();
        		
        		List<Animal> listAnimal = AnimalQuery.getListAnimalByIdAndTypeId(id, "enclosureid");
        		
        		for(Animal x : listAnimal) {
        			int animalId = x.getId();
        			AnimalHealthQuery.delete(animalId,"animalid");
        			AnimalDietQuery.delete(animalId, "animalid");
        		}
    			
    			EmployeeEnclosureQuery.delete(id, "enclosureid");
    			AnimalQuery.delete(id,"enclosureid");
    			
				EnclosureQuery.delete(id);
				JOptionPane.showMessageDialog(this, "Xóa thành công");
				refesh();
			} catch (Exception e) {
				JOptionPane.showMessageDialog(this, "Xóa thất bại");
				e.printStackTrace();
			}
        }
    }                                        

