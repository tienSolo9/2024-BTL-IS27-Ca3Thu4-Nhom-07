    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {  

        try {
        	Employee e = new Employee(null, null, null, 0, null, null, null, null, null, null);
        	e.setId(Integer.parseInt(jTextField10.getText()));
        	e.setName(jTextField8.getText());
    		e.setShift(jComboBox2.getSelectedItem().toString());
    		e.setDateOfBirth(jTextField6.getText());
    		e.setDateOfJoining(jTextField9.getText());
    		e.setPhone(jTextField11.getText());
    		e.setDesignation(jComboBox1.getSelectedItem().toString());
    		e.setUsername(jTextField12.getText());
    		e.setPassword(new String(jPasswordField1.getPassword()));
    		e.setGender(jTextField7.getText());
			EmployeeQuery.update(e);
			
			String str = jTextField13.getText();
			String[] listId = str.split(",");
			List<Integer> enclosureids = new ArrayList<>();
	
			for(String x : listId) {
				boolean check = true;
					if(!x.equals("")) {
					check = isNumber.check(x);
					if(check == false) {
						throw new Exception();
						// kiem tra xem neu input khong hop le thi return
					}
					enclosureids.add(Integer.parseInt(x));
				}
			}
			for(int x : enclosureids) {
				EnclosureQuery.isExists(x);
			}
			
			EmployeeEnclosureQuery.delete(e.getId(),"employeeid");
			
			for(int x : enclosureids) {
				EmployeeEnclosure a = new EmployeeEnclosure();
				a.setEnclosureid(x);
				a.setEmployeeid(e.getId());
				EmployeeEnclosureQuery.add(a);
			}
			
			JOptionPane.showMessageDialog(this, "Cập nhật thành công");
			this.dispose();
		} catch (SQLException e1) {
			JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
			e1.printStackTrace();
		} catch (Exception e1) {
			JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
			e1.printStackTrace();
		}
        
        
    }                                        

